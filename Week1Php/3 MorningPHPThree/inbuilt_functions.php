<?php

$greeting = "Hello World";
echo($greeting);
echo "<br>";

//fn for uppercase
echo strtoupper($greeting);
echo "<br>";

//fn for lowercase
echo strtolower($greeting);
echo "<br>";

//fn for reverse
echo strrev($greeting);
echo "<br>";

//fn for encryption
echo md5($greeting);
echo "<br>";

//fn for square of a number
$number = 20;
